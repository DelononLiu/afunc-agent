#ifndef ADD_H
#define ADD_H

#include <cstdint>

/**
 * @brief 整数加法函数
 * 
 * @param a 第一个整数
 * @param b 第二个整数
 * @return int32_t 两个整数的和
 */
int32_t addi(int32_t a, int32_t b);

/**
 * @brief 浮点数加法函数
 * 
 * @param a 第一个浮点数
 * @param b 第二个浮点数
 * @return float 两个浮点数的和
 */
float addf(float a, float b);

#endif // ADD_H