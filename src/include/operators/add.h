#ifndef OPERATORS_ADD_H
#define OPERATORS_ADD_H

/**
 * @brief 整数加法
 * @param a 第一个加数
 * @param b 第二个加数
 * @return int 两个整数的和
 */
int add_int(int a, int b);

/**
 * @brief 浮点数加法
 * @param a 第一个加数
 * @param b 第二个加数
 * @return float 两个浮点数的和
 */
float add_float(float a, float b);

#endif // OPERATORS_ADD_H